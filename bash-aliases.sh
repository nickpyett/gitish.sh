# Short bash aliases for git.

alias adda="gitish::add_a"
alias addn="gitish::add_n"
alias bran="gitish::branch_name"
alias chod="git checkout develop"
alias chom="git checkout master"
alias coma="git add -A && git commit"
alias comi="git commit"
alias comn="gitish::commit_n"
alias difh="git diff HEAD > gitish-diff-head.diff && open gitish-diff-head.diff"
alias newb="gitish::branch_new"
alias pull="git pull"
alias push="git push"
alias setu="gitish::set_upstream"
alias stsn="gitish::status_n"
alias unsn="gitish::unstage_n"
